/**
 * Given an array of domains, return the object with the appearances of the DNS.
 *
 * @param {Array} domains
 * @return {Object}
 *
 * @example
 * domains = [
 *  'code.yandex.ru',
 *  'music.yandex.ru',
 *  'yandex.ru'
 * ]
 *
 * The result should be the following:
 * {
 *   '.ru': 3,
 *   '.ru.yandex': 3,
 *   '.ru.yandex.code': 1,
 *   '.ru.yandex.music': 1,
 * }
 *
 */
function getDNSStats(domains) {
   let max = domains
   .map((el)=>el
   .match(/\w+./g).length)
   .sort()
   .reverse()[0];
   return domains.join(" ").match(new RegExp(`(\.\w+){${max-1},}`,"gm"));
}

module.exports = {
  getDNSStats
};
